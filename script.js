function list() {
  let container = document.querySelector(".container");
  programmers.forEach(function (programmer) {
    container.innerHTML += `<div class="card">      
        <img src="${programmer.image || "./img/person-icon.jpg"}" alt="person-icon"> 
        <div class = "bio">
        <span className="firstName">${programmer.firstName}</span>
        <span className="lastName">${programmer.lastName}</span>
         </div>
          <div class="btn">
          <a href="#bioPage?=${programmer.id}" class="biolink"><button>Read More</button></a>
          </div>
          
          </div>`;
  });
}

